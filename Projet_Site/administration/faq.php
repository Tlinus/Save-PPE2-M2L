<h1>5. Et ensuite ?</h1>
<p>
    Si vous répondez à un sujet, assurez-vous d'avoir lu complètement le message auquel vous répondez ainsi que les échanges ayant déjà eu lieu avant d'envoyer votre commentaire.

    Évitez l'impatience. Inutile de revenir trop tôt à la charge sous prétexte que votre question n'a pas encore de réponse. Quelqu'un, quelque part, est sûrement en train de chercher.

    Évitez les messages privés pour tenter d'obtenir une réponse plus rapidement. Cela ne profite pas aux autres visiteurs du forum. Les questions, comme les réponses, ont pour but d'accroître la base de connaissances de la communauté.

    Utilisez la fonction [Réglé] : ainsi les problèmes ayant été résolus seront facilement identifiés dans le forum.

    Dire merci ! Ce n'est pas grand chose, mais quand quelqu'un vous a apporté, par sa réponse, l'aide que vous attendiez, revenez dire merci. Ca ne coûte rien, ça fait plaisir et ça renforce les liens de la communauté. Accessoirement, ça encourage même les gens à continuer de s'entre-aider .
    </p>
