<h1>7. Autres règles d’usage du forum</h1>
<h2>7.1 Inscription</h2>
<p>
Un seul compte par personne est autorisé.

Lors de l'enregistrement, et pendant toute la vie du compte, une adresse électronique valable doit être utilisée. Les adresses électroniques jetables ne sont pas autorisées.

Les noms d'utilisateur sous forme de nom de domaine sont interdits.

N’utilisez pas de pseudo pouvant laissant supposer que vous êtes quelqu’un d’autre,
</p>
<h2>7.2 Signature :</h2>
<p>
    Elle doit être paramétrée dans votre panneau de contrôle (Mon profil -> Ma signature), et non ajoutée manuellement à vos messages.
    Son texte doit respecter les règles générales énoncées pour le contenu, notamment ce qui concerne les activités illégales, le respect des personnes, et la promotion.
    Elle ne doit comporter d’adresse postale, de numéro de téléphone, d’e-mail ou autres moyen de vous contacter directement (afin de vous protéger des spams).
    Elle ne doit pas contenir d’image ; elle peut contenir un maximum de deux smileys. Les tailles de police utilisées ne peuvent dépasser celle la police normale du forum, et vous pouvez utiliser au maximum deux couleurs.
    Elle ne doit pas faire plus de 4 lignes (sur un écran en résolution 1024).
    La signature ne doit pas contenir plus de 3 liens cliquables ; ces liens doivent être exprimés sous la forme d’une URL visible (http://www.M2L.fr est OK, pas M2L!fr)
    Nota : tout lien renvoyant sur l'un des sites de l'AFUJ ou de M2L.fr n'est pas comptabilisé dans ce quota (exemple lien pour signaler le M2L).
    Nous nous réservons le droit de vous demander de modifier et/ou de supprimer les éléments de votre signature qui contreviendraient aux règles mentionnées ci-dessus.
</p>
<h2>7.3 Sanctions</h2>
<p>
Les messages enfreignant les présentes règles pourront être déplacés, modifiés ou supprimés sans avertissement ni avis. Les avatars incorrects seront supprimés de la même façon.

Vous serez invité à modifier votre signature si celle-ci n’est pas conforme. En l’absence de réponse, ou de manquement grave, nous pourrons procéder à la modification ou à la suppression de la signature en cause.

Les utilisateurs contrevenant de façon grave ou répétée aux règles du forum pourront voir leur compte suspendu ou supprimé.
</p>
<h2>7.4 Propriété du contenu</h2>
<p>
 Lorsque vous avez publié un message sur le forum, le contenu du message entre dans le cadre du domaine public, la suppression de son contenu ne pourra être fait que par l’auteur lui même et les demandes de suppressions par un administrateur ne seront pas validées.

Vous pouvez par contre demander la suppression de votre compte, et l’anonymisation de vos anciens messages.

Pour ce faire, veuillez nous contacter grâce au lien suivant : http://forum.M2L.fr/sendmessage.php
</p>
<h2>7.5 Présentation de vos sites</h2>
<p>
La rubrique VOS SITES permet de présenter votre site réalisé grâce à M2L.

Nous vous demandons de présenter le site dans sa version finalisée : présenter un site vide de contenu ou dont le template est encore à l'état de brouillon ne présente pas grand intérêt pour les visiteurs.


Si vous voulez un avis sur un site en cours de construction, postez dans le forum adéquat [nom du sous forum]. Les questions concernant la gestion des composants, des templates... sont à poser dans les sous forum qui leur sont dédiés.
</p>
<h2>7.6.1 Obligation de présentation :</h2>
<p>
Vous devez impérativement mentionner sur votre présentation les éléments suivants :

   <br> Adresse du site (clicable) : dans le corps du poste, pas dans le titre !
   <br> Version de M2L utilisée
   <br> Template utilisé
    <br>Extensions utilisées
    <br>Hébergeur
    <br>Description de votre site
    <br>Optionnel : Ce que vous voulez qu'on critique de votre site (design, code, graphisme...)
<br><br>
Ces règles sont le plus souvent respectées, à l'exception de la demande de présentation des extensions. Cela a pour effet d'induire des séries de messages inutiles ("tu as utilisé quoi pour faire ci, ou pour faire çà").

Les extensions comprennent les composants, les modules ainsi que les plugins additionnels.
</p>
<h2>7.6.2 Sont interdit dans cette section :</h2>
<p>
    Il est expressément interdit de faire un "up" sur la présentation de son site.
    En outre, les sites ne peuvent être présenté qu'une seule fois. S'il y a une mise à jour de votre site, veuillez éditer votre premier message et non créer un nouveau message (et encore moins une nouvelle discussion).
    Les messages contraires à ces recommandations seront effacés ou fusionnés.
    Il est interdit de mettre des images/logo de votre site dans les messages !
</p>
<h2>7.7 Seront supprimé sans avertissements</h2>
<p>
    Les trolls
    Les messages ne respectant pas la Netiquette ou comportant insultes et propos diffamants.
    Les liens pointant vers des sites à caractère illégal (warez...) et ceux pouvant porter atteinte aux bonnes moeurs (sites pour adulte...).
    Les messages traitant de sujets graves (faits divers...) n'ayant aucun rapport avec le ton (bonne humeur) et l'orientation (support M2L!) du forum.
    Tout message que nous estimerons être de l'autopromotion, de la publicité ou du SPAM pourra et sera supprimé. NI SPAM, NI PUBLICITÉ. Un message qui ferait des références inutiles, déplacées ou trop nombreuses à vos produits ou sites sera considéré comme de l'autopromotion.
</p>
