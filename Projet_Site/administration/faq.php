<h1>4. Comment poser une question ?</h1>
<h2>4.1 Avant de poster</h2>
<p>
Effectuez une recherche avant de poser une question (formulaire en haut à droite), la solution à votre problème a peut-être déjà été trouvée, ou la discussion est peut-être déjà ouverte. Une FAQ, des tutoriels et une section documentation sont consultables sur .M2L.fr. Vous y trouverez peut-être la réponse à votre question </p>
<h2>4.2 Choisissez la bonne section.</h2>
<p>
Choisissez bien la section en fonction du sujet de discussion, en lisant bien la description de chaque partie. Ce forum est composé de plusieurs espaces dédiés à une thématique particulière, merci de les respecter, notamment soyez vigilants sur les versions.

Si vous ne trouvez pas, ou si vous avez un doute sur le lieu où poster, postez dans celui qui vous semble le plus proche du sujet de votre question, ou contactez un administrateur (le cas échéant, cela pourra donner naissance à une nouvelle rubrique).

Ne parasitez pas une discussion en cours, ne relancez pas une discussion terminée juste parce qu’elle a un vague rapport avec votre question : celui nuit autant à la lisibilité du forum qu’à la visibilité de votre question. Ouvrez plutôt un nouveau sujet.

Postez votre question une seule fois ; en particulier ne posez pas la même question sur plusieurs forums.
</p>
<h2>4.3 Choisissez un titre explicite</h2>
<p>
Choisissez un titre approprié et explicite, permettant de qualifier le sujet au premier coup d’œil. « Aidez-moi », « Au secours !!!!! », « Gros problème ! » ne sont pas des titres appropriés. ». Un bon titre pourrait être « L'icône d'aide ne s'affiche plus dans la page de configuration globale ». Nous vous conseillons également d'utiliser un des préfixes prévus pour préciser la nature du problème.

Répétez le sujet dans le corps du texte si cela rend les choses plus claires Précisez dans votre message la version de M2L utilisée, si vous êtes en local ou chez un hébergeur, le cas échéant, précisez également le nom, la version de l’extension qui pose problème. Incluez autant d'informations que vous le pouvez. Ceci peut inclure les logs et le résultat de lignes de commande. Personne ne pourra vous aider si vous ne donnez pas de détails suffisants. N'hésitez pas également à remplir la liste de tag en bas du message pour qu'il puisse être facilement trouvable par d'autres utilisateurs. Gardez à l'esprit que plus vous donnerez de détails plus vous pourrez obtenir facilement une réponse pertinente.

Nous vous informons que les modérateurs pourront modifier un titre s'ils le jugent nécessaire (et s'ils ont le temps). Par ailleurs, si un membre ne fait manifestement pas d'effort pour choisir les titres de ses discussions, elles pourront être verrouillées (ce qui l'empêchera d'avoir des réponses), le temps qu'il mette à jour lui-même son titre.
</p>
<h2>4.4 Un modérateur n’est pas une machine à réponse !</h2>
<p>
Gardez bien à l’esprit que tous les participants de ce forum (utilisateurs, modérateurs, administrateurs) sont des bénévoles qui donnent de leur temps libre pour répondre aux questions, pour gérer le forum. Les modérateurs ne sont pas tenus de vous répondre (surtout s’ils n’en ont pas le temps).

Si votre message reste désespérément sans réponses, pensez à vérifier s’il est dans la bonne section (par exemple un message concernant un composant dans “tout et rien” ne sera probablement pas lu), et si son titre est suffisamment explicite (“A l’aide” ne l’est absolument pas !). Ne vous énervez pas, et ne hurlez pas au scandale comme quoi on vous ignore (si si il y en a qui ont tenté de le faire). Pour indication, il y a près d’un millier de messages par jour, nous ne pouvons pas tous les lire. Et puis aussi, même si nous le lisons, nous n’avons pas la science infuse et il arrive (souvent) que nous ne connaissons pas nous même la réponse à votre problème... mais d’autres utilisateurs du forum oui ! (ils vous répondront eux :D )
</p>
