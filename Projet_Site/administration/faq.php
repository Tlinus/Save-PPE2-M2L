<h1>6. Rédaction et mise en forme</h1>
<p>
    Tous les messages doivent être rédigés en Français
    Utilisez un langage clair et compréhensible. Évitez le style SMS, les jargons techniques et les patois locaux. Faites des phrases, avec des mots, des verbes, des adjectifs, des majuscules (mais pas trop), des accents, de la ponctuation... Relisez-vous, corrigez-vous. Cela augmente vos chance d’être lus et compris, et montre le respect que vous accordez aux autres.
    Employez vos propres phrases. Si vous souhaitez employer les phrases de quelqu'un d'autre, citez-les, en cliquant sur « citer » (QUOTE). Le plagiat n'est pas éthique et est illégal dans beaucoup de pays.
    Utilisez pour le corps du texte la typographie par défaut. Ne mettez pas tout votre message en majuscules, gras, grande police, couleur vive : l’effet risque d’être l’inverse de l’effet souhaité, irritation du lecteur et lisibilité moindre. Utilisez les attributs de caractère à bon escient et avec parcimonie.
    Si vous incluez du code (HTML, PHP, CSS, SQL… ) dans votre message, utilisez des indentations et mettez-le ente balises CODE ou PHP.
    Pièces jointes : il est préférable d'utiliser le système de pièces jointes du forum, qui assurent notamment un redimensionnement des images en fonction du site.
    Les URL (adresses internet) doivent être en clair dans les messages, c'est à dire qu'il est interdit d'utiliser un raccourcisseur d'URL, ou de mettre un lien du type portail M2L. Seul est autorisé http://www.M2L.fr.
   </p>
