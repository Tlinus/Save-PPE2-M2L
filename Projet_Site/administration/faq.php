<h1> Préambule : </h1>
<p>
Bien que les administrateurs et les modérateurs de m2L.fr essaient d'écarter tout message répréhensible de ce forum, il leur est impossible de contrôler tous les messages. Ceux-ci expriment uniquement les opinions de leur auteur. L’Association des Utilisateurs de M2L (propriétaire de ce forum) et M2L.org (développeurs de M2L) n'entendent donner ni approbation, ni improbation des propos émis sur ce forum. Ils ne sauraient être considérés comme responsables du contenu des messages dont ils ne sont pas les auteurs.

En acceptant ces règles, vous vous engagez à n'écrire aucun message à caractère obscène, vulgaire, discriminatoire, menaçant, diffamatoire, injurieux ou contraire aux lois et règlements en vigueur.

Vous autorisez les administrateurs de M2L.fr et ses modérateurs à supprimer, modifier, déplacer ou fermer n'importe quel message pour n'importe quelle raison et sans autorisation préalable de votre part.

Un forum est un espace de convivialité où l'on vient échanger, chercher des réponses à des questions, proposer des solutions…

Les règles regroupées ici ont pour but de faire en sorte que les échanges restent conviviaux et efficaces, et que la masse d’informations disponible soit accessible facilement.

Les règles sont simples, l’équipe d’administration et de modération vous demande simplement de les respecter sous peine de sanctions. Toute inscription comme membre suppose l'acceptation sans réserve et le respect des règles du forum.

L'équipe administrative se réserve le droit de changer ces règles à tout moment.
</p>
